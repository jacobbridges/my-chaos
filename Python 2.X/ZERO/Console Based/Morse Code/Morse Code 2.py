def main():
    s = stdin.readline()
    while s:
        translate(s)
        s = stdin.readline()
    
def translate(s):
    s = ''.join(c for c in s.upper() if c in CODE)
    for i, c in enumerate(s):
        if c == ' ':
            sleep(SEP)
        else:
            for d in CODE[c][:-1]:
                Beep(FQC, int((DOT if d == '.' else DAH) * 1000))
                sleep(DOT)
            Beep(FQC, int((DOT if CODE[c][-1] == '.' else DAH) * 1000))
            if i + 1 != len(s) and s[i + 1] != ' ':
                sleep(DAH)

################################################################################
                
if __name__ == '__main__':
    main()
